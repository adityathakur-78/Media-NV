// Arrays, tuples , enums
// arrays - in this we can define the same data types for all ararys
let numbers: number[] = [1, 2, 4, 5];
console.log(numbers);

let str3: Array<string> = ["Aditya", "Amit", "Shivam"];
console.log(str3);

// tuple - here order matters we can define many data types in single arrr
let ton: [string, number, boolean] = ["Aditya", 1, false];
console.log(ton);
